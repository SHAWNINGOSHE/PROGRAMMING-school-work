class Assignmentoperator {
public static void main(String[] args) {
// create variables
int a = 4;
int b;
// assign value using =
b = a;
System.out.println("b using =: " + b);
// assign value using =+
b += a;
System.out.println("b using +=: " + b);
// assign value using =*
b *= a;
System.out.println("b using *=: " + b);
}
}